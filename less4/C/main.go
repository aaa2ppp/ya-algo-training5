package main

import (
	"bufio"
	"io"
	"log"
	"os"
	"sort"
	"strconv"
	"unsafe"
)

func search(sum []int, l, s int) int {

	n := len(sum) - l // массив префиксных сумм на 1 элемент больше, поэтому +1 здесь не нужен
	if n < 1 {
		return -1
	}

	i, found := sort.Find(n, func(i int) int {
		return s - (sum[i+l] - sum[i])
	})

	if !found {
		return -1
	}

	return i + 1 // to 1-indexing
}

func run(in io.Reader, out io.Writer) error {
	sc := bufio.NewScanner(in)
	sc.Split(bufio.ScanWords)
	bw := bufio.NewWriter(out)
	defer bw.Flush()

	n, m, err := scanTwoInt(sc)
	if err != nil {
		return err
	}

	sum := make([]int, n+1)
	for i := 0; i < n; i++ {
		a, err := scanInt(sc)
		if err != nil {
			return err
		}
		sum[i+1] = sum[i] + a
	}

	for i := 0; i < m; i++ {
		l, s, err := scanTwoInt(sc)
		if err != nil {
			return err
		}
		k := search(sum, l, s)
		writeInt(bw, k, writeOpts{end: '\n'})
	}

	return nil
}

